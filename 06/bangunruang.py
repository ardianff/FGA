def keliling_kubus (sisi) :
    return 12*sisi
def luas_kubus (sisi) :
    return 6*(pow(sisi, 2))
def volume_kubus (sisi) :
    return pow(sisi,3)
def keliling_balok(p,l,t) :
    return 4*(p+l+t)
def luas_balok(p,l,t) :
    return 2*((p*l)+(p*t)+(l*t))
def volume_balok(p,l,t) :
    return p*l*t
